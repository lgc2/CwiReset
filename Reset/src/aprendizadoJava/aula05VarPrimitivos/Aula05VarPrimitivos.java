package aprendizadoJava.aula05VarPrimitivos;

public class Aula05VarPrimitivos {
	
	public static void main(String[] args) {
		byte idade01 = 20;
		
		short idade02 = 21;
		
		int idade03 = 22;
		
		long idade04 = 23;
		
		System.out.println("Valor 01 = " + idade01);
		System.out.println("Valor 02 = " + idade02);
		System.out.println("Valor 03 = " + idade03);
		System.out.println("Valor 04 = " + idade04);
	}
	


}
